/**
 * report — Generates the top-level BENCHMARKS.md from reports/summary.json.
 *
 * Run after measure.ts. Reads machine-readable results and produces a
 * marketing-friendly markdown report at the marketplace root.
 */

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = dirname(__filename);

const BENCHMARKS_DIR = join(__dirname, '..');
const MARKETPLACE_ROOT = join(BENCHMARKS_DIR, '..');
const SUMMARY_PATH = join(BENCHMARKS_DIR, 'reports/summary.json');
const REPOS_PATH = join(BENCHMARKS_DIR, 'repos.json');
const OUTPUT_PATH = join(MARKETPLACE_ROOT, 'BENCHMARKS.md');

interface BrickStat {
    name: string;
    tool_count: number;
    tokens: number;
}

interface TaskResult {
    id: string;
    title: string;
    baseline_tokens: number;
    focus_tokens: number;
    savings_tokens: number;
    savings_pct: number;
    required_bricks: string[];
}

interface SummaryReport {
    meta: {
        generated_at: string;
        catalog_version: string;
        brick_count: number;
        tokenizer: string;
    };
    bricks: BrickStat[];
    tasks: TaskResult[];
}

interface RepoEntry {
    id: string;
    language: string;
    profile: string;
    url: string;
    ref: string;
    loc_estimate: number;
}

function formatNum(n: number): string {
    return n.toLocaleString('en-US');
}

function pct(p: number): string {
    return p.toFixed(1);
}

function maxSavings(tasks: TaskResult[]): TaskResult {
    return tasks.reduce((best, t) => (t.savings_pct > best.savings_pct ? t : best));
}

function generate(): void {
    const summary = JSON.parse(readFileSync(SUMMARY_PATH, 'utf-8')) as SummaryReport;
    const repos = JSON.parse(readFileSync(REPOS_PATH, 'utf-8')) as RepoEntry[];

    const { meta, bricks, tasks } = summary;
    const best = maxSavings(tasks);
    const avgSavings = tasks.reduce((s, t) => s + t.savings_pct, 0) / tasks.length;
    const tokenizerNote =
        meta.tokenizer === 'cl100k_base'
            ? 'cl100k_base BPE (same as GPT-4/Claude)'
            : 'char-based heuristic (~4 chars per token — approximate)';

    const taskTableRows = tasks
        .map(
            (t) =>
                `| ${t.title} | ${formatNum(t.baseline_tokens)} | ${formatNum(t.focus_tokens)} | **${pct(t.savings_pct)}%** |`,
        )
        .join('\n');

    const sortedBricks = [...bricks].sort((a, b) => b.tokens - a.tokens);
    const brickTableRows = sortedBricks
        .map((b, i) => `| ${i + 1} | \`${b.name}\` | ${b.tool_count} | ${formatNum(b.tokens)} |`)
        .join('\n');

    const repoTableRows = repos
        .map(
            (r) =>
                `| \`${r.id}\` | ${r.language} | ${r.profile} | [link](${r.url}) | \`${r.ref}\` | ~${formatNum(r.loc_estimate)} |`,
        )
        .join('\n');

    const md = `<!-- SPDX-FileCopyrightText: 2026 FocusMCP contributors -->
<!-- SPDX-License-Identifier: MIT -->
<!-- Auto-generated by \`pnpm benchmark\` — do not edit manually -->

# FocusMCP Benchmarks

Token-cost validation for the "200k → ~2k tokens" claim.
Auto-generated from \`benchmarks/reports/summary.json\`.

> Generated: ${meta.generated_at} | Catalog: ${meta.catalog_version} | Bricks: ${meta.brick_count} | Tokenizer: ${tokenizerNote}

## Headline

For typical agent tasks, FocusMCP reduces the tool-definition context by **~${pct(avgSavings)}% on average**
(best case: **${pct(best.savings_pct)}%** for "${best.title}").

| Task | Without FocusMCP | With FocusMCP | Savings |
|---|---:|---:|---:|
${taskTableRows}

## Methodology

Each brick exposes its tools as a JSON array (name + description + inputSchema).
This is exactly what an MCP server sends in a \`tools/list\` response.
We serialize that array to JSON and count its BPE tokens using **${tokenizerNote}**.

**Baseline** = sum of all ${meta.brick_count} bricks' tool-definition tokens — the cost paid by an agent
that loads the full marketplace without FocusMCP.

**Focus** = sum of only the bricks required for a specific task — the cost paid when
FocusMCP selects the relevant subset at runtime.

**Savings** = (baseline − focus) / baseline × 100.

## Per-brick breakdown

${meta.brick_count} bricks, sorted by token cost (descending).

| Rank | Brick | Tools | Tokens |
|---:|---|---:|---:|
${brickTableRows}

## Target repositories (Phase 2)

These ${repos.length} repositories will be cloned and benchmarked in Phase 2 (real execution).
No cloning is performed in Phase 1 — this is metadata only.

| Repo | Language | Profile | URL | Ref | LOC (est.) |
|---|---|---|---|---|---:|
${repoTableRows}

## Reproducing

\`\`\`bash
pnpm benchmark
\`\`\`

This runs:
1. \`tsx benchmarks/src/measure.ts\` — reads every \`bricks/*/mcp-brick.json\`, counts tokens, writes \`benchmarks/reports/summary.json\` and per-task reports.
2. \`tsx benchmarks/src/report.ts\` — reads \`summary.json\`, writes this file (\`BENCHMARKS.md\`).

## Limitations

- **Phase 1 only**: measures static token cost of tool definitions, not runtime context windows.
- **No input data**: does not count tokens from actual file contents fed to the agent during a task.
- **Tokenizer**: ${tokenizerNote}. Counts may differ slightly from actual model billing.
- **Brick selection**: the \`required_bricks\` sets in \`benchmarks/tasks.json\` are manually curated expert estimates, not measured from real agent sessions.
- **Phase 2** will add real repo execution: clone target repos, run each task scenario with and without FocusMCP, and measure actual context window usage.
`;

    writeFileSync(OUTPUT_PATH, md, 'utf-8');
    console.log('Wrote BENCHMARKS.md');
}

generate();
